# 2'. Напишите программу для проверки истинности утверждения ¬(X ⋁ Y ⋁ Z) = ¬X ⋀ ¬Y ⋀ ¬Z
# для всех значений предикат.
# ⋁ - "Или"
# ⋀ - "И"
# ¬ - "Не"
def task2():
    print('Задание 2')
    x = int(input('Введите значение X (1 - истина, 0 - ложь): '))
    y = int(input('Введите значение Y (1 - истина, 0 - ложь): '))
    z = int(input('Введите значение Z (1 - истина, 0 - ложь): '))
    x = (x == 1)
    y = (y == 1)
    z = (z == 1)
    print(f'X = {x}, Y = {y}, Z = {z}')
    result1 = not (x or y or z)
    result2 = (not x) and (not y) and (not z)
    print('Значение ¬(X ⋁ Y ⋁ Z):', result1)
    print('Значение ¬X ⋀ ¬Y ⋀ ¬Z:', result2)
    if result1 == result2:
        print('Значения равны')
    else:
        print('Значения не равны')


# 3'. Напишите программу, которая принимает на вход координаты точки (X и Y), причём X ≠ 0 и Y ≠ 0 и
# выдаёт номер четверти плоскости, в которой находится эта точка (или на какой оси она находится).
#
# *Пример:*
# - x=34; y=-30 -> 4
# - x=2; y=4-> 1
# - x=-34; y=-30 -> 3
def input_coord(coord):
    n = 0
    while n == 0:
        n = int(input(f'Введите воординату {coord}: '))
        if n == 0:
            print('Неверный ввод! Введите значение заново')
    return n


