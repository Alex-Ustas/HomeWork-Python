    a = list(map(int, input('Координаты точки A (через пробел): ').split()))
    b = list(map(int, input('Координаты точки B (через пробел): ').split()))
    if len(a) != 2 or len(b) != 2:
        print('Неверное количество координат')
    else:
        dist = ((a[0] - b[0]) ** 2 + (a[1] - b[1]) ** 2) ** 0.5
        print(f'Расстояние между координатами {a} и {b} =', round(dist, 2))


